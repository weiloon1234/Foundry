fn action_accepts_request_payload(action: &ContractAction) -> bool {
    action.request.is_some()
}

fn action_method(action: &ContractAction) -> &str {
    match &action.transport {
        ContractTransport::Http(http) => http.method.as_deref().unwrap_or("get"),
        ContractTransport::WebSocket(_) => "get",
    }
}

fn action_params_type(action: &ContractAction) -> String {
    match &action.transport {
        ContractTransport::Http(http) if http.path_params.is_empty() => {
            "Record<never, never>".to_string()
        }
        ContractTransport::Http(http) => {
            let fields = http
                .path_params
                .iter()
                .map(|param| format!("{}: RouteParamValue", json_string(param)))
                .collect::<Vec<_>>();
            format!("{{ {} }}", fields.join("; "))
        }
        ContractTransport::WebSocket(_) => "Record<never, never>".to_string(),
    }
}

fn sdk_body_kind_literal(kind: ContractHttpBody) -> &'static str {
    match kind {
        ContractHttpBody::None => "none",
        ContractHttpBody::Json => "json",
        ContractHttpBody::Multipart => "multipart",
        ContractHttpBody::Unknown => "unknown",
    }
}

fn render_route_helper(
    helper: &PlannedRouteHelper<'_>,
    schema_exports: &BTreeMap<String, serde_json::Value>,
    exported_types: &BTreeMap<String, String>,
    validations: &BTreeMap<&'static str, TsValidationSchema>,
) -> Result<String> {
    let route = helper.route;
    let name = &helper.name;
    let endpoint_name = route_type_name(name, "Endpoint");
    let request_alias = route_type_name(name, "Request");
    let response_alias = route_type_name(name, "Response");
    let params_alias = route_type_name(name, "Params");
    let path_const = route_type_name(name, "Path");
    let method_const = route_type_name(name, "Method");
    let validation_const = route_type_name(name, "Validation");

    let mut imports = BTreeSet::new();
    let request_type = schema_type_reference(
        route.request,
        exported_types,
        &mut imports,
        "Record<string, never>",
    );
    let response_type = route_response_type(route, schema_exports, exported_types, &mut imports);
    let params_type = render_route_params_type(route);

    let import_lines = imports
        .iter()
        .filter_map(|import| {
            exported_types.get(import).map(|file| {
                route_helper_module_specifier(file).map(|module| {
                    format!("import type {{ {import} }} from {};", json_string(&module))
                })
            })
        })
        .collect::<Result<Vec<_>>>()?;

    let validation = render_validation_schema(
        route.request.and_then(|request| validations.get(request)),
        &request_alias,
    );
    let method = option_string_literal(route.method.as_deref());
    let has_params = !route.params.is_empty();
    let has_request = route.request.is_some();
    let factory = match (has_params, has_request) {
        (true, true) => format!(
            "export function {name}(client: FoundryHttpClient, params: {params_alias}, data: {request_alias}): {endpoint_name} {{\n\
               return new {endpoint_name}(client, data as {request_alias} & Record<string, unknown>, params);\n\
             }}\n"
        ),
        (true, false) => format!(
            "export function {name}(client: FoundryHttpClient, params: {params_alias}, data: {request_alias} = {{}} as {request_alias}): {endpoint_name} {{\n\
               return new {endpoint_name}(client, data as {request_alias} & Record<string, unknown>, params);\n\
             }}\n"
        ),
        (false, true) => format!(
            "export function {name}(client: FoundryHttpClient, data: {request_alias}): {endpoint_name} {{\n\
               return new {endpoint_name}(client, data as {request_alias} & Record<string, unknown>);\n\
             }}\n"
        ),
        (false, false) => format!(
            "export function {name}(client: FoundryHttpClient, data: {request_alias} = {{}} as {request_alias}): {endpoint_name} {{\n\
               return new {endpoint_name}(client, data as {request_alias} & Record<string, unknown>);\n\
             }}\n"
        ),
    };

    Ok(format!(
        "{TYPE_FILE_HEADER}\n\n\
         import {{ FoundryEndpoint, type FoundryHttpClient, type FoundryHttpMethod, type FoundryValidationSchema }} from \"../FoundryEndpoint\";\n\
         import type {{ RouteName, RouteParamValue }} from \"../RouteManifest\";\n\
         {}\n\
         export const {path_const} = {} as const;\n\
         export const {method_const} = {} as FoundryHttpMethod | null;\n\
         export type {request_alias} = {request_type};\n\
         export type {response_alias} = {response_type};\n\
         export type {params_alias} = {params_type};\n\n\
         export const {validation_const} = {validation};\n\n\
         export class {endpoint_name} extends FoundryEndpoint<{request_alias} & Record<string, unknown>, {response_alias}, {params_alias}> {{\n\
           readonly routeName = {} as RouteName;\n\
           readonly path = {path_const};\n\
           readonly method = {method_const};\n\
           readonly validation = {validation_const};\n\
         }}\n\n\
         {factory}",
        import_lines.join("\n"),
        json_string(&route.path),
        method,
        json_string(route.id.as_str()),
        factory = factory,
    ))
}

fn render_sdk_action(
    action: &PlannedSdkAction<'_>,
    schema_exports: &BTreeMap<String, serde_json::Value>,
    exported_types: &BTreeMap<String, String>,
) -> Result<String> {
    let name = &action.name;
    let action_name = route_type_name(name, "Action");
    let request_alias = route_type_name(name, "SdkRequest");
    let response_alias = route_type_name(name, "SdkResponse");
    let params_alias = route_type_name(name, "SdkParams");
    let definition_const = route_type_name(name, "ActionDefinition");
    let factory_name = format!("create{name}Action");

    let mut imports = BTreeSet::new();
    let request_type = schema_type_reference(
        action
            .action
            .request
            .as_ref()
            .map(|request| request.schema.as_str()),
        exported_types,
        &mut imports,
        "Record<string, never>",
    );
    let response_type =
        action_response_type(action.action, schema_exports, exported_types, &mut imports);
    let params_type = action_params_type(action.action);
    let import_lines = imports
        .iter()
        .filter_map(|import| {
            exported_types.get(import).map(|file| {
                route_helper_module_specifier(file).map(|module| {
                    format!("import type {{ {import} }} from {};", json_string(&module))
                })
            })
        })
        .collect::<Result<Vec<_>>>()?;

    let body = sdk_body_kind_literal(action_body_kind(action.action));
    let method = action_method(action.action);
    let route_id = &action.action.id;

    let accepts_request_payload = action_accepts_request_payload(action.action);

    let action_signature = if accepts_request_payload {
        format!(
            "(request: {request_alias}, options?: FoundryActionOptions<{params_alias}>) => Promise<{response_alias}>"
        )
    } else {
        format!("(options?: FoundryActionOptions<{params_alias}>) => Promise<{response_alias}>")
    };

    let action_body = if accepts_request_payload {
        format!(
            "  return (request, options = {{}}) => sendFoundryAction<{request_alias}, {response_alias}, {params_alias}>(transport, {definition_const}, request, mergeFoundryActionOptions(defaults, options));\n"
        )
    } else {
        format!(
            "  return (options = {{}}) => sendFoundryAction<{request_alias}, {response_alias}, {params_alias}>(transport, {definition_const}, undefined, mergeFoundryActionOptions(defaults, options));\n"
        )
    };

    Ok(format!(
        "{TYPE_FILE_HEADER}\n\n\
         import {{ mergeFoundryActionOptions, sendFoundryAction, type FoundryActionDefinition, type FoundryActionOptions, type FoundryClientOptions, type FoundrySdkTransport }} from \"../FoundrySdk\";\n\
         import type {{ RouteName, RouteParamValue }} from \"../RouteManifest\";\n\
         {}\n\
         export type {request_alias} = {request_type};\n\
         export type {response_alias} = {response_type};\n\
         export type {params_alias} = {params_type};\n\
         export type {action_name} = {action_signature};\n\n\
         export const {definition_const} = {{\n\
           routeName: {} as RouteName,\n\
           method: {},\n\
           body: {},\n\
         }} as const satisfies FoundryActionDefinition<{params_alias}>;\n\n\
         export function {factory_name}(transport: FoundrySdkTransport, defaults?: FoundryClientOptions): {action_name} {{\n\
         {action_body}\
         }}\n",
        import_lines.join("\n"),
        json_string(route_id),
        json_string(method),
        json_string(body),
    ))
}

fn render_sdk_client(actions: &[PlannedSdkAction<'_>]) -> Result<String> {
    let mut imports = vec![
        "import type { FoundryClientOptions, FoundrySdkTransport } from \"./FoundrySdk\";"
            .to_string(),
    ];
    for action in actions {
        let module = sdk_action_module_specifier(&action.file)?;
        imports.push(format!(
            "import {{ create{}Action }} from {};",
            action.name,
            json_string(&module)
        ));
    }

    let entries = actions
        .iter()
        .map(|action| {
            format!(
                "    {}: create{}Action(transport, defaults)",
                action.function_name, action.name
            )
        })
        .collect::<Vec<_>>();
    let client_entries = if entries.is_empty() {
        String::new()
    } else {
        format!("{},\n", entries.join(",\n"))
    };

    Ok(format!(
        "{TYPE_FILE_HEADER}\n\n\
         {}\n\n\
         export function createFoundryClient(transport: FoundrySdkTransport, defaults?: FoundryClientOptions) {{\n\
           return {{\n{}\
           }} as const;\n\
         }}\n\n\
         export type FoundryClient = ReturnType<typeof createFoundryClient>;\n",
        imports.join("\n"),
        client_entries,
    ))
}

/// Export all registered TypeScript types to a directory.
pub fn export_all(dir: &Path) -> Result<()> {
    export_all_with_routes(dir, &[])
}

/// Export all registered TypeScript types and HTTP route metadata to a directory.
pub fn export_all_with_routes(dir: &Path, routes: &[RouteManifestEntry]) -> Result<()> {
    export_all_with_context(dir, routes, TypeScriptExportContext::default())
}

/// Export all registered TypeScript types plus HTTP, realtime, and config metadata.
pub fn export_all_with_context(
    dir: &Path,
    routes: &[RouteManifestEntry],
    context: TypeScriptExportContext,
) -> Result<()> {
    std::fs::create_dir_all(dir).map_err(Error::other)?;

    let ts_types: Vec<&TsType> = inventory::iter::<TsType>.into_iter().collect();
    let app_enums: Vec<&TsAppEnum> = inventory::iter::<TsAppEnum>.into_iter().collect();
    let ts_type_files = planned_ts_type_files(&ts_types)?;
    let validations = validation_schemas();
    let contract = contract_manifest(routes, &validations, context.realtime_channels.clone())?;
    let schema_exports = contract
        .schemas
        .iter()
        .map(|schema| (schema.name.clone(), schema.schema.clone()))
        .collect::<BTreeMap<_, _>>();
    let route_helpers = planned_route_helpers(routes)?;
    let sdk_actions = planned_sdk_actions(&contract)?;
    let output_files =
        planned_output_files(&ts_type_files, &app_enums, &route_helpers, &sdk_actions);
    clean_manifest_files(dir, &output_files)?;

    let mut type_exports = BTreeMap::new();
    for ts_type in ts_types {
        (ts_type.export_fn)(dir)
            .map_err(|e| Error::message(format!("ts export `{}`: {e}", ts_type.name)))?;
        let file = ts_type_files
            .get(ts_type.name)
            .expect("planned TypeScript file should exist")
            .clone();
        postprocess_ts_type_file(&dir.join(&file), validations.get(ts_type.name))?;
        type_exports.insert(ts_type.name, file);
    }

    // Rewrite AppEnum files entirely — if ts-rs also emitted an enum file,
    // the metadata-based AppEnum export owns the final file content.
    let mut enum_names = HashSet::new();
    let mut grouped_enum_names = HashSet::new();
    for app_enum in app_enums {
        let file_path = dir.join(format!("{}.ts", app_enum.name));
        let rendered = render_app_enum(app_enum.name, &(app_enum.meta_fn)())?;
        if rendered.has_groups {
            grouped_enum_names.insert(app_enum.name);
        }
        write_generated_file(&file_path, rendered.content)?;
        enum_names.insert(app_enum.name);
    }

    let mut exported_types = BTreeMap::new();
    for (name, file) in &type_exports {
        exported_types.insert((*name).to_string(), file.clone());
    }
    for name in &enum_names {
        exported_types.insert((*name).to_string(), format!("{name}.ts"));
    }

    let mut names: Vec<&str> = type_exports
        .keys()
        .copied()
        .chain(enum_names.iter().copied())
        .collect();
    names.sort();
    names.dedup();

    write_generated_file(
        &dir.join("RouteManifest.ts"),
        render_route_manifest(routes)?,
    )?;
    write_generated_file(&dir.join(ENDPOINT_RUNTIME_FILE), render_endpoint_runtime())?;
    write_generated_file(&dir.join(ERROR_RUNTIME_FILE), render_error_runtime())?;
    write_generated_file(&dir.join(SDK_RUNTIME_FILE), render_sdk_runtime())?;
    write_generated_file(
        &dir.join(I18N_MANIFEST_FILE),
        render_i18n_manifest(context.i18n.as_ref())?,
    )?;
    write_generated_file(
        &dir.join(WEBSOCKET_MANIFEST_FILE),
        render_websocket_manifest(&contract.realtime_channels, &exported_types)?,
    )?;
    write_generated_file(
        &dir.join(DATATABLE_MANIFEST_FILE),
        render_datatable_manifest(&context.datatable_ids)?,
    )?;
    write_generated_file(
        &dir.join(CONTRACT_MANIFEST_FILE),
        serde_json::to_string_pretty(&contract).map_err(Error::other)?,
    )?;

    if !route_helpers.is_empty() {
        std::fs::create_dir_all(dir.join(ROUTE_HELPER_DIR)).map_err(Error::other)?;
    }
    for helper in &route_helpers {
        write_generated_file(
            &dir.join(&helper.file),
            render_route_helper(helper, &schema_exports, &exported_types, &validations)?,
        )?;
    }
    if !sdk_actions.is_empty() {
        std::fs::create_dir_all(dir.join(SDK_ACTION_DIR)).map_err(Error::other)?;
    }
    for action in &sdk_actions {
        write_generated_file(
            &dir.join(&action.file),
            render_sdk_action(action, &schema_exports, &exported_types)?,
        )?;
    }
    write_generated_file(&dir.join(SDK_CLIENT_FILE), render_sdk_client(&sdk_actions)?)?;

    let mut barrel = format!("{TYPE_FILE_HEADER}\n");
    for name in &names {
        if enum_names.contains(name) {
            let groups_export = if grouped_enum_names.contains(name) {
                format!(", {name}Groups")
            } else {
                String::new()
            };
            barrel.push_str(&format!(
                "export {{ type {name}, {name}Keys, {name}Values, {name}Options, {name}Meta{groups_export} }} from \"./{name}\";\n"
            ));
        } else {
            let file = type_exports
                .get(name)
                .expect("planned TypeScript export should exist");
            let module = ts_module_specifier(file)?;
            barrel.push_str(&format!("export type {{ {name} }} from \"{module}\";\n"));
        }
    }
    barrel.push_str(
        "export { RouteManifest, RouteIds, createRouteUrlBuilder, routeUrl, type EmptyRouteParams, type RouteName, type RouteParams, type RouteParamValue, type RouteUrlOptions } from \"./RouteManifest\";\n",
    );
    barrel.push_str(
        "export { FoundryEndpoint, FoundryValidationClientError, setFoundryValidationTranslator, type FoundryContractValueKind, type FoundryFieldErrors, type FoundryHttpClient, type FoundryHttpHeaders, type FoundryHttpMethod, type FoundryHttpRequestConfig, type FoundryHttpResponse, type FoundrySubmitOptions, type FoundryValidationAttribute, type FoundryValidationField, type FoundryValidationMessage, type FoundryValidationRule, type FoundryValidationSchema, type JsonValue } from \"./FoundryEndpoint\";\n",
    );
    barrel.push_str(
        "export { FoundrySdkError, type FoundryErrorCode, type FoundryErrorResponse, type FoundryFieldError, type FoundryValidationErrors } from \"./FoundryErrors\";\n",
    );
    barrel
        .push_str("export { createFoundryClient, type FoundryClient } from \"./FoundryClient\";\n");
    barrel.push_str(
        "export { mergeFoundryActionOptions, sendFoundryAction, type FoundryActionDefinition, type FoundryActionOptions, type FoundryClientOptions, type FoundrySdkBodyKind, type FoundrySdkHeaders, type FoundrySdkHttpMethod, type FoundrySdkRequestConfig, type FoundrySdkResponse, type FoundrySdkTransport } from \"./FoundrySdk\";\n",
    );
    barrel.push_str("export * from \"./I18nManifest\";\n");
    barrel.push_str("export * from \"./WebSocketChannelManifest\";\n");
    barrel.push_str("export * from \"./DatatableManifest\";\n");
    for helper in &route_helpers {
        let module = ts_module_specifier(&helper.file)?;
        barrel.push_str(&format!("export * from \"{module}\";\n"));
    }
    for action in &sdk_actions {
        let module = ts_module_specifier(&action.file)?;
        barrel.push_str(&format!("export * from \"{module}\";\n"));
    }
    write_generated_file(&dir.join("index.ts"), barrel)?;
    write_export_manifest(dir, &output_files)?;

    println!("Exported {} type(s) to {}", names.len(), dir.display());

    Ok(())
}

fn planned_ts_type_files(ts_types: &[&TsType]) -> Result<BTreeMap<&'static str, String>> {
    let mut files = BTreeMap::new();
    for ts_type in ts_types {
        let file = ts_type_output_file(ts_type)?;
        if let Some(existing) = files.insert(ts_type.name, file.clone()) {
            if existing != file {
                return Err(Error::message(format!(
                    "TypeScript export `{}` registered multiple output paths: `{existing}` and `{file}`",
                    ts_type.name
                )));
            }
        }
    }
    Ok(files)
}

fn planned_output_files(
    ts_type_files: &BTreeMap<&'static str, String>,
    app_enums: &[&TsAppEnum],
    route_helpers: &[PlannedRouteHelper<'_>],
    sdk_actions: &[PlannedSdkAction<'_>],
) -> BTreeSet<String> {
    let mut files = BTreeSet::new();
    for file in ts_type_files.values() {
        files.insert(file.clone());
    }
    for app_enum in app_enums {
        files.insert(format!("{}.ts", app_enum.name));
    }
    files.insert(CONTRACT_MANIFEST_FILE.to_string());
    files.insert("RouteManifest.ts".to_string());
    files.insert(ENDPOINT_RUNTIME_FILE.to_string());
    files.insert(ERROR_RUNTIME_FILE.to_string());
    files.insert(SDK_RUNTIME_FILE.to_string());
    files.insert(SDK_CLIENT_FILE.to_string());
    files.insert(I18N_MANIFEST_FILE.to_string());
    files.insert(WEBSOCKET_MANIFEST_FILE.to_string());
    files.insert(DATATABLE_MANIFEST_FILE.to_string());
    for helper in route_helpers {
        files.insert(helper.file.clone());
    }
    for action in sdk_actions {
        files.insert(action.file.clone());
    }
    files.insert("index.ts".to_string());
    files
}

fn ts_type_output_file(ts_type: &TsType) -> Result<String> {
    let path = (ts_type.output_path_fn)().ok_or_else(|| {
        Error::message(format!(
            "TypeScript export `{}` does not expose an output path",
            ts_type.name
        ))
    })?;
    let file = path.to_str().ok_or_else(|| {
        Error::message(format!(
            "TypeScript export `{}` output path must be valid UTF-8",
            ts_type.name
        ))
    })?;

    let Some(path) = safe_manifest_path_with_extension(file, "ts", true) else {
        return Err(Error::message(format!(
            "TypeScript export `{}` output path `{file}` must be a relative .ts path inside the generated types directory",
            ts_type.name
        )));
    };

    Ok(path.to_string_lossy().replace('\\', "/"))
}

fn ts_module_specifier(file: &str) -> Result<String> {
    let module = file.strip_suffix(".ts").ok_or_else(|| {
        Error::message(format!(
            "TypeScript barrel export path `{file}` must end with .ts"
        ))
    })?;
    Ok(format!("./{module}"))
}

fn clean_manifest_files(dir: &Path, output_files: &BTreeSet<String>) -> Result<()> {
    clean_generated_manifest_files(
        dir,
        TYPES_EXPORT_MANIFEST,
        output_files,
        "foundry.typescript",
        safe_typescript_manifest_path,
    )
}

fn safe_typescript_manifest_path(file: &str) -> Option<PathBuf> {
    safe_manifest_path_with_extension(file, "ts", true).or_else(|| {
        if file == CONTRACT_MANIFEST_FILE {
            safe_manifest_path_with_extension(file, "json", false)
        } else {
            None
        }
    })
}

fn write_export_manifest(dir: &Path, output_files: &BTreeSet<String>) -> Result<()> {
    write_manifest(dir, TYPES_EXPORT_MANIFEST, output_files)
}

fn collect_route_manifest(routes: &[RouteRegistrar]) -> Result<Vec<RouteManifestEntry>> {
    let mut registrar = HttpRegistrar::new();
    for route in routes {
        route(&mut registrar)?;
    }
    registrar.collect_route_manifest()
}

/// CLI registrar for the `types:export` command.
pub fn builtin_cli_registrar(
    routes: Vec<RouteRegistrar>,
    websocket_routes: Vec<WebSocketRouteRegistrar>,
) -> CommandRegistrar {
    Arc::new(move |registry| {
        let routes = routes.clone();
        let websocket_routes = websocket_routes.clone();
        registry.command(
            TYPES_EXPORT_COMMAND,
            clap::Command::new("types:export")
                .about("Export registered TypeScript types")
                .arg(
                    clap::Arg::new("output")
                        .long("output")
                        .short('o')
                        .help("Output directory (overrides config)"),
                ),
            move |invocation| {
                let routes = routes.clone();
                let websocket_routes = websocket_routes.clone();
                async move {
                    let output = if let Some(dir) = invocation.matches().get_one::<String>("output")
                    {
                        PathBuf::from(dir)
                    } else {
                        let config = invocation.app().config().typescript().unwrap_or_default();
                        PathBuf::from(config.output_dir)
                    };

                    let route_manifest = collect_route_manifest(&routes)?;
                    let ws_registrar = crate::websocket::build_registrar(&websocket_routes)?;
                    let ws_registry =
                        crate::websocket::WebSocketChannelRegistry::from_registrar(ws_registrar);
                    let realtime_channels = ws_registry
                        .descriptors()
                        .iter()
                        .map(ContractRealtimeChannel::from)
                        .collect::<Vec<_>>();
                    let i18n_config = invocation.app().config().i18n().unwrap_or_default();
                    let locales = invocation
                        .app()
                        .i18n()
                        .map(|manager| {
                            manager
                                .locale_list()
                                .into_iter()
                                .map(str::to_string)
                                .collect::<Vec<_>>()
                        })
                        .unwrap_or_else(|_| {
                            vec![
                                i18n_config.default_locale.clone(),
                                i18n_config.fallback_locale.clone(),
                            ]
                        });
                    let datatable_ids = invocation
                        .app()
                        .datatables()
                        .map(|registry| {
                            registry
                                .ids()
                                .into_iter()
                                .map(str::to_string)
                                .collect::<Vec<_>>()
                        })
                        .unwrap_or_default();
                    export_all_with_context(
                        &output,
                        &route_manifest,
                        TypeScriptExportContext {
                            realtime_channels,
                            i18n: Some(I18nTypeScriptManifest {
                                default_locale: i18n_config.default_locale,
                                fallback_locale: i18n_config.fallback_locale,
                                locales,
                            }),
                            datatable_ids,
                        },
                    )
                }
            },
        )?;
        Ok(())
    })
}

